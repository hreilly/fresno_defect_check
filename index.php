    <h3>WHAT IS A DEFECT?</h3>
    <p>Although many homes that are involved in construction defect litigation are part of a Complaint in which every major part of the home is claimed to be defective (see actual Complaints from court files), California law takes a more narrow view of defects for which damages may be awarded. Generally speaking, an alleged defect must allow damage to be caused to another part of the house before a valid claim can be stated. For example, a roof leak which causes staining or damage to interior drywall is an example of a recoverable defect. A cracked sidewalk which does not allow resulting damage is not a recoverable defect.</p>
